uint64
sys_exit(void)
{
  int n;
  argint(0, &n);
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  argaddr(0, &p);
  return wait(p);
}

uint64
sys_sbrk(void)
{
  uint64 addr;
  int n;

  argint(0, &n);
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  if(n < 0)
    n = 0;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

// 当调用 trace 的 pid 还存活时，打印 syscall trace，该 pid 死后，不再打印
uint64
sys_trace(void)
{
  int syscall_mask;
  struct proc *p = myproc();

  // 根据源码，是根据第一个参数来决定获取 trapframe 的 a0/a1/a2/a3...
  // trapframe 的 a0/a1/a2/3... 就是系统调用时使用的参数，由用户空间设置
  argint(0, &syscall_mask);

  // 根据 syscall_mask 设置当前进程的 syscall_mask
  p->syscall_mask = syscall_mask;

  // 永远成功
  return 0;
}
